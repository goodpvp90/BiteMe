package enums;

/**
 * The EnumDish enum represents the different dish types available in the restaurant menu.
 */
public enum EnumDish {

    /**
     * Represents an appetizer dish type.
     */
    APPETIZER,

    /**
     * Represents a main course dish type.
     */
    MAIN_COURSE,

    /**
     * Represents a dessert dish type.
     */
    DESSERT,

    /**
     * Represents a beverage dish type.
     */
    BEVERAGE,

    /**
     * Represents a salad dish type.
     */
    SALAD;
}
